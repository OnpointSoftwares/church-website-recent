// Counter Animation
function animateCounters() {
    const counters = document.querySelectorAll('.counter');
    const speed = 200; // Animation speed

    counters.forEach(counter => {
        const target = +counter.getAttribute('data-target');
        const count = +counter.innerText;
        const inc = target / speed;

        if (count < target) {
            counter.innerText = Math.ceil(count + inc);
            setTimeout(() => animateCounters(), 1);
        } else {
            counter.innerText = target;
        }
    });
}

// Intersection Observer for animations
const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.style.opacity = '1';
            entry.target.style.transform = 'translateY(0)';
            
            // Trigger counter animation for stats section
            if (entry.target.classList.contains('stats-showcase')) {
                setTimeout(animateCounters, 500);
            }
        }
    });
}, observerOptions);

// Observe all fade-in elements
document.querySelectorAll('.fade-in').forEach(el => {
    el.style.opacity = '0';
    el.style.transform = 'translateY(30px)';
    el.style.transition = 'opacity 0.8s ease, transform 0.8s ease';
    observer.observe(el);
});

// Gallery item interactions
document.querySelectorAll('.gallery-item').forEach(item => {
    item.addEventListener('click', function() {
        const title = this.querySelector('.gallery-overlay h4').textContent;
        const description = this.querySelector('.gallery-overlay p').textContent;
        
        // Create a simple modal-like alert (you can replace with a proper modal)
        const message = `${title}\n\n${description}\n\nWould you like to learn more about this aspect of our ministry?`;
        if (confirm(message)) {
            window.location.href = 'mailto:soundmedia@christekklesians.org?subject=Interest in ' + title;
        }
    });
});

// Express Interest function
function showInterest() {
    const name = prompt("What's your name?");
    if (name) {
        const phone = prompt("What's your phone number?");
        if (phone) {
            const experience = prompt("Do you have any audio/visual experience? (Yes/No)");
            const subject = `Ministry Interest - ${name}`;
            const body = `Hello,\n\nI'm interested in joining the Sound & Media Ministry.\n\nName: ${name}\nPhone: ${phone}\nExperience: ${experience || 'Not specified'}\n\nPlease contact me with more information about getting involved.\n\nThank you!`;
            
            window.location.href = `mailto:soundmedia@christekklesians.org?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;
        }
    }
}

// Enhanced hover effects for contact items
document.querySelectorAll('.contact-item').forEach(item => {
    item.addEventListener('mouseenter', function() {
        this.style.transform = 'translateX(15px) scale(1.02)';
        this.style.boxShadow = '0 10px 25px rgba(0, 0, 0, 0.1)';
    });
    
    item.addEventListener('mouseleave', function() {
        this.style.transform = 'translateX(0) scale(1)';
        this.style.boxShadow = 'none';
    });
});

// Smooth scrolling for internal links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});

